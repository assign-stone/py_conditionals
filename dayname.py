# WAP to print day name according to a number

no = int(input("Enter the number btw 1-7: "))
if no == 1:
    print("Monday")
elif no == 2:
    print("Tuesday")
elif no == 3:
    print("Wednesday")
elif no == 4:
    print("Thursday")
elif no == 5:
    print("Friday")
elif no == 6:
    print("Saturday")
elif no == 7:
    print("Sunday")
else:
    print("THERE IS NO DAY ON THIS NUMBER")